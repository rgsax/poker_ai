package it.unical.poker.a; 

public class CardAdapter {
	String suit; 
	int value; 
	
	CardAdapter() { 
		
	}

	public String getSuit() {
		return suit;
	}

	public void setSuit(String suit) {
		this.suit = suit;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}
	
}
